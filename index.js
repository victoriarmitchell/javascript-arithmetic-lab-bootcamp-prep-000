function add(a,b) {
  return a+b
}

function subtract(a,b) {
  return a-b
}

function multiply(a,b) {
  return a*b
}

function divide(a,b) {
  return a/b
}

function inc(n) {
  n += 1;
  return n;
}

function dec(n) {
  n -= 1;
  return n;
}


function makeInt(n) {
  n = '0x2328';
  parseInt(n,10);
  return n;
  
}


function preserveDecimal(n) {
  parseFloat(n);
  return n;
}